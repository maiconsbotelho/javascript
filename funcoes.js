// FUNCTION DECLARATION OU FUNÇÃO NOMEADA
function bomDia() {
  console.log("Bom dia!");
  return "Bom dia!";
}

// FUNCTION EXPRESSION OU FUNÇÃO ANÔNIMA
const boaTarde = function () {
  console.log("Boa tarde!");
  return "Boa tardee!";
};

// ARROW FUNCTION
const boaNoite = () => {
  console.log("Boa noite!");
  return "Boa noite!";
};

bomDia();
boaTarde();
boaNoite();
